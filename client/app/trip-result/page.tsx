"use client";

import { useEffect, useState } from "react";
import { useSearchParams, useRouter } from "next/navigation";
import ReactMarkdown from "react-markdown";
import { getTripById } from "@/services/tripApi";
import ProtectedRoute from "@/components/Auth/ProtectedRoute";

export default function TripResultPage() {
  const searchParams = useSearchParams();
  const router = useRouter();

  const [trip, setTrip] = useState<any>(null);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    const fetchTrip = async () => {
      try {
        const id = searchParams.get("id");

        if (id) {
          // Opened from My Trips
          const data = await getTripById(id);
          setTrip(data.trip);
        } else {
          // Opened immediately after generating a trip
          const saved = localStorage.getItem("trip");

          if (saved) {
            const data = JSON.parse(saved);
            setTrip(data.trip || data);
          }
        }
      } catch (error) {
        console.error(error);
      } finally {
        setLoading(false);
      }
    };

    fetchTrip();
  }, [searchParams]);

  if (loading) {
    return (
      <div className="min-h-screen flex justify-center items-center text-2xl">
        Loading...
      </div>
    );
  }

  if (!trip) {
    return (
      <div className="min-h-screen flex justify-center items-center text-2xl">
        Trip not found.
      </div>
    );
  }

  return (
    <ProtectedRoute>
    <main className="min-h-screen bg-gradient-to-br from-sky-100 to-white p-10">
      <div className="max-w-6xl mx-auto">

        <div className="flex justify-between items-center mb-8">

          <div>
            <h1 className="text-6xl font-bold">
              🌍 Your AI Travel Plan
            </h1>

            <p className="text-gray-600 mt-2 text-lg">
              Generated by VoyageAI
            </p>
          </div>

          <button
            onClick={() => router.push("/planner")}
            className="bg-blue-600 hover:bg-blue-700 text-white px-8 py-4 rounded-2xl text-xl"
          >
            Plan Another Trip
          </button>

        </div>

        <div className="bg-white rounded-3xl shadow-2xl p-10 prose prose-lg max-w-none">

          <ReactMarkdown>
            {trip.itinerary}
          </ReactMarkdown>

        </div>

      </div>
    </main>
    </ProtectedRoute>
  );
}